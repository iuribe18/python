# Literal Types
# 1. Integers
# Octal Numbers
octal = 0o123 # Octal
# Hexadecimal Numbers
hexa = 0x123 # Hexadecimal
a = 10        # Entero
b = 3.14      # Flotante
c = 3 + 4j    # Complejo
print(a)
print(b)
print(c)
print(octal)
print(hexa)

# Literales de cadena
greeting = "Hello, World!"
print(greeting)

# Literales booleanos
is_valid = True
no_valid = False
print(is_valid)
print(no_valid)

# Literal especial
nothing = None
print(nothing)

# Literales de colecci√≥n
